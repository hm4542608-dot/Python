#Stripping whitespace in python
#1.strip() removes spaces from both sides
my_name = " Hassaan Mahmood "
print(my_name.strip())
#2.lstrip() removes spaces from the left side only
your_name = " Hayyaan Mahmood"
print(your_name.strip())
#3.rstrip() removes spaces from the right side only
his_name = "Habbaan Mahmood "
print(his_name.strip()) 